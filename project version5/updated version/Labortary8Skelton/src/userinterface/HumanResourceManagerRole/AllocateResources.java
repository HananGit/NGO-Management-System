    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean b = checkLimit();
        if(b)
        {
            
            for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
                if (o instanceof VolunteerOrganization) {
                    for (UserAccount ua : o.getUserAccountDirectory().getUserAccountList()) {
                        for (VolunteerWork vw : wr.getVolunteerList()) {
                            if (vw.getUserName().equals(ua.getUsername())) {
                                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                                Date d = new Date(wr.getEventDate());
                                String dateVariable = sdf.format(d);
                                ua.getEventDatesList().add(dateVariable);
                            }
                        }
                    }
                }

            }
            wr.setStatus("Planned");
            enterprise.getEventList().add(wr);
            ua.getWorkQueue().getWorkRequestList().add(wr);
            JOptionPane.showMessageDialog(null,"Volunteers have been added to the event");
            backJButton.doClick();
            
        }
            else
        {
            JOptionPane.showMessageDialog(null,"Check total needed and total assigned volunteers");
            
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

