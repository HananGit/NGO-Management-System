    private void populateAssignedVolunteers() {
        DefaultTableModel dtm = (DefaultTableModel) jTableAssigned.getModel();
        dtm.setRowCount(0);
        int count = 0;
        
        for (VolunteerWork v : wr.getVolunteerList()) {

            Object[] o = new Object[3];
            o[0] = v;
            o[1] = v.getUserName();
            o[2] = v.getSkills();
            dtm.addRow(o);
            count++;
        
        }
        jTextFieldTotalAssigned.setEnabled(true);
        jTextFieldTotalAssigned.setText(String.valueOf(count));
        jTextFieldTotalAssigned.setEnabled(false);
        
        
        populateUnassignedVolunteers();
        
    }

    private void populateUnassignedVolunteers() {
        DefaultTableModel dtm = (DefaultTableModel) JTableAvailable.getModel();
        dtm.setRowCount(0);
        boolean flag = true;
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (o instanceof VolunteerOrganization) {
                for (UserAccount ua : o.getUserAccountDirectory().getUserAccountList()) {
                    flag = true;
                    for (VolunteerWork v : wr.getVolunteerList()) {
                        if (v.getName().equals(ua.getName())) {
                            flag = false;
                        }

                    }
                    if (flag) {
                        Object[] row = new Object[3];
                        row[0] = ua;
                        row[1] = ua.getName();
                        row[2] = ua.getSkillSet();
                        dtm.addRow(row);

                    }

                }

            }
        }
    }

